     private void inicializarProductos() {
        // Obtener los nombres y contadores de los productos
        List<String> nombresProductos = controlador.obtenerNombresProductos();
        Map<String, Integer> datosProductos = controlador.obtenerDatosProductos();

        // Producto 1
        if (nombresProductos.size() > 0) {
            String nombreProducto1 = nombresProductos.get(0);
            int contadorVotos1 = datosProductos.get(nombreProducto1);

            jLabel3.setText(nombreProducto1);
            etiquetasContadores.put(nombreProducto1, iFIQ);
            iFIQ.setText(String.valueOf(contadorVotos1));

            jButton1.addActionListener(new ActionListener() {
                @Override
                public void actionPerformed(ActionEvent e) {
                    controlador.procesarVoto(nombreProducto1);
                    controlador.registrarEnBitacora("App.inicializarProdcutos()", "Se proceso el voto para "+ nombreProducto1);
                }
            });
        }

        // Producto 2
        if (nombresProductos.size() > 1) {
            String nombreProducto2 = nombresProductos.get(1);
            int contadorVotos2 = datosProductos.get(nombreProducto2);

            jLabel4.setText(nombreProducto2);
            etiquetasContadores.put(nombreProducto2, iFMAT);
            iFMAT.setText(String.valueOf(contadorVotos2));

            jButton2.addActionListener(new ActionListener() {
                @Override
                public void actionPerformed(ActionEvent e) {
                    controlador.procesarVoto(nombreProducto2);
                    controlador.registrarEnBitacora("App.inicializarProdcutos()", "Se proceso el voto para "+ nombreProducto2);

                }
            });
        }

        // Producto 3
        if (nombresProductos.size() > 2) {
            String nombreProducto3 = nombresProductos.get(2);
            int contadorVotos3 = datosProductos.get(nombreProducto3);

            jLabel5.setText(nombreProducto3);
            etiquetasContadores.put(nombreProducto3, iFI);
            iFI.setText(String.valueOf(contadorVotos3));

            jButton3.addActionListener(new ActionListener() {
                @Override
                public void actionPerformed(ActionEvent e) {
                    controlador.procesarVoto(nombreProducto3);
                    controlador.registrarEnBitacora("App.inicializarProdcutos()", "Se proceso el voto para "+ nombreProducto3);

                }
            });
        }
        
        controlador.registrarEnBitacora("App.inicializarProductos()", "Se inicializaron los productos correctamente");
    }
    
    // M√©todo para actualizar la vista con los datos actualizados
    public void actualizarVista(Map<String, Integer> datosProductos) {
        for (Map.Entry<String, Integer> entrada : datosProductos.entrySet()) {
            String nombreProducto = entrada.getKey();
            int contadorVotos = entrada.getValue();
            JLabel etiquetaContador = etiquetasContadores.get(nombreProducto);
            if (etiquetaContador != null) {
                etiquetaContador.setText(String.valueOf(contadorVotos));
            }
        }
    }
    
